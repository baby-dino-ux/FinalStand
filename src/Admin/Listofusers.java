    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
    
    // Get selected row
    int selectedRow = jTable1.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, 
            "Please select a user to delete!", 
            "No Selection", 
            JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // Get user details
    int userId = Integer.parseInt(jTable1.getValueAt(selectedRow, 0).toString());
    String username = jTable1.getValueAt(selectedRow, 3).toString();
    
    // Confirm deletion
    int confirm = JOptionPane.showConfirmDialog(this, 
        "Are you sure you want to delete user '" + username + "'?\n\nThis action cannot be undone!", 
        "Confirm Deletion", 
        JOptionPane.YES_NO_OPTION,
        JOptionPane.WARNING_MESSAGE);
    
    if (confirm == JOptionPane.YES_OPTION) {
        // Delete from database
        config dbConfig = new config();
        String sql = "DELETE FROM tbl_users WHERE u_id=?";
        dbConfig.deleteRecord(sql, userId);
        
        JOptionPane.showMessageDialog(this, 
            "User deleted successfully!", 
            "Success", 
            JOptionPane.INFORMATION_MESSAGE);
        
        // Refresh table
        loadUserData();
    }

    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
         // Get selected row
    // Get selected row
    int selectedRow = jTable1.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, 
            "Please select a user to edit!", 
            "No Selection", 
            JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // Get current values from selected row
    int userId = Integer.parseInt(jTable1.getValueAt(selectedRow, 0).toString());
    String currentFirstname = jTable1.getValueAt(selectedRow, 1).toString();
    String currentLastname = jTable1.getValueAt(selectedRow, 2).toString();
    String currentUsername = jTable1.getValueAt(selectedRow, 3).toString();
    String currentEmail = jTable1.getValueAt(selectedRow, 4).toString();
    String currentPassword = jTable1.getValueAt(selectedRow, 5).toString();
    String currentType = jTable1.getValueAt(selectedRow, 6).toString();
    
    // Create input dialogs for editing
    String firstname = JOptionPane.showInputDialog(this, "First Name:", currentFirstname);
    if (firstname == null) return; // User cancelled
    
    String lastname = JOptionPane.showInputDialog(this, "Last Name:", currentLastname);
    if (lastname == null) return;
    
    String username = JOptionPane.showInputDialog(this, "Username:", currentUsername);
    if (username == null) return;
    
    String email = JOptionPane.showInputDialog(this, "Email:", currentEmail);
    if (email == null) return;
    
    String password = JOptionPane.showInputDialog(this, "Password:", currentPassword);
    if (password == null) return;
    
    // Dropdown for user type
    String[] types = {"User", "Admin"};
    String type = (String) JOptionPane.showInputDialog(
        this,
        "Select User Type:",
        "User Type",
        JOptionPane.QUESTION_MESSAGE,
        null,
        types,
        currentType
    );
    if (type == null) return;
    
    // Validate inputs
    if (firstname.trim().isEmpty() || lastname.trim().isEmpty() || 
        username.trim().isEmpty() || email.trim().isEmpty() || password.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, 
            "All fields are required!", 
            "Validation Error", 
            JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // Update in database
    config dbConfig = new config();
    String sql = "UPDATE tbl_users SET firstname=?, lastname=?, username=?, email=?, password=?, type=? WHERE u_id=?";
    dbConfig.updateRecord(sql, firstname, lastname, username, email, password, type, userId);
    
    JOptionPane.showMessageDialog(this, 
        "User updated successfully!", 
        "Success", 
        JOptionPane.INFORMATION_MESSAGE);
    
    // Refresh table
    loadUserData();

    }//GEN-LAST:event_btnEditActionPerformed

